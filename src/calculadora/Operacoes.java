package calculadora;

public class Operacoes {

    float soma(float n1, float n2) {

        return n1 + n2;
    }

    float subtracao(float n1, float n2) {
        return n1 - n2;
    }

    float multiplicacao(float n1, float n2) {
        return n1 * n2;
    }

    float divisao(float n1, float n2) {
        return n1 / n2;
    }
    float resto(float n1, float n2){
        return n1 % n2;
    }
}
